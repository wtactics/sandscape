<h2>Welcome to SandScape</h2>

<p>
    This is the default starting page for <strong>Sandscape</strong>. You can edit this text in the 
    <em>/sandscape/views/site/pages/home.php</em> file.
</p>

<p>
    The purpose of <strong>Sandscape</strong> project is to create a virtual, online 
    and purely browser based, environment where players can meet to play a CCG. 
    <strong>Sandscape</strong> is, as all things <strong>WTactics</strong>, an open source 
    endeavor that should be generic, easy to extend and modify so that it can be 
    of use in other projects, making it possible to play almost any CCG with it.
</p>

<p>
    <strong>Sandscape</strong> is not just about the cards created in <strong>WTactics</strong>, it's 
    about creating a complete, independent system that allows players to gather and arrange 
    matches, local communities to create an on-line location for their members to continue 
    playing, and is not limited to the <strong>WTactics</strong> main rule set, we 
    want <strong>Sandscape</strong> to be able to offer any game or rule set that users choose.
</p>

<p>
    If you need help setting up your <strong>Sandscape</strong> installation, please visit our 
    forum at <a href="http://www.wtactics.org/forum/viewforum.php?f=14">http://www.wtactics.org/forum/viewforum.php?f=14</a>.
</p>

<p>
    Learn more about <strong>Sandscape</strong> at  <a href="http://sourceforge.net/projects/sandscape">http://sourceforge.net/projects/sandscape</a> 
    or visit the <strong>WTactis</strong> project at <a href="http://wtactics.org">http://wtactics.org</a>.
</p>
